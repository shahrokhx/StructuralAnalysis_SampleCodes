% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
L  = str2num(get(handles.edit1,'String'));
A1 = str2num(get(handles.edit2,'String'));
A2 = str2num(get(handles.edit3,'String'));
t  = str2num(get(handles.edit4,'String'));

E  = str2num(get(handles.edit5,'String'));
rho= str2num(get(handles.edit6,'String'));
P  = str2num(get(handles.edit7,'String'));
n  = str2num(get(handles.edit8,'String'));

x0 = str2num(get(handles.edit9,'String'));
bc = [get(handles.checkbox1,'Value'), get(handles.checkbox2,'Value')];
bc = double(~bc);
[nodeData,elementData] = inpFileGen(L,A1,A2,t,E,rho,P,x0,n,bc);
Solution = planarTrussSolver('inputFile.txt');
dx = Solution.nodalDisplacements(:,2);
f  = Solution.internalForces.axial;
set(handles.uitable1,'Data',[nodeData, dx])
set(handles.uitable2,'Data',[elementData, f])
f = figure(5);
grid on;
hold on;
colors = ['b','r','g','m','k'];
c = colors(floor(rand()*length(colors)+1));
plot(abs(nodeData(:,1)),abs(dx),['-*',c]);
title('Vertical Displacements')
xlabel('|Y| coordinate')
ylabel('|v| displacement')


